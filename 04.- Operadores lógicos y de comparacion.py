# Operadores lógicos y de comparacion

# Operdores lógicos
# and
# or
# not

es_estudiante = True
trabaja = False
print(es_estudiante or trabaja)
print(not es_estudiante)

# Operadores de comparación
numero_1 = 5
numero_2 = 2

print(numero_1 == numero_2)
print(numero_1 != numero_2)
print(numero_1 > numero_2)
print(numero_1 < numero_2)
print(numero_1 >= numero_2)
print(numero_1 <= numero_2)

