# Cadena de caracteres

nombre = input("Ingrese un texto: ")

# Mayúsculas
nombre = nombre.upper()
print(nombre)

# Miníscula
nombre = nombre.lower()
print(nombre)

# Capitalizar
nombre = nombre.capitalize()
print(nombre)

# Reemplazar caracteres
nombre = nombre.replace(' ', ',')
print(nombre)

# Hola,mundo
print(nombre[3])