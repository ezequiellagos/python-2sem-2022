# Operadores aritmeticos

# Suma
print("Suma")
print(2 + 3)

# Resta
print("Resta")
print(2 - 3)

# Multiplicacion
print("Multiplicacion")
print(2 * 3)

# Division
print("Division")
print(2 / 3)

# Modulo
print("Modulo")
print(2 % 3)

# Potencia
print("Potencia")
print(2 ** 3)

# Raiz cuadrada
print("Raiz cuadrada")
print(2 ** 0.5)